    let currentLang = 'ge';

function switchLanguage(lang) {
      currentLang = lang;
      // Update document language attribute for accessibility/SEO
      document.documentElement.setAttribute('lang', lang === 'ge' ? 'ka' : 'en');
      document.body.className = lang === 'ge' ? 'georgian' : '';
      document.getElementById('lang-en').classList.toggle('active', lang === 'en');
      document.getElementById('lang-ge').classList.toggle('active', lang === 'ge');
      document.getElementById('lang-en').setAttribute('aria-pressed', lang === 'en');
      document.getElementById('lang-ge').setAttribute('aria-pressed', lang === 'ge');
      // Update all translatable elements
      document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        const translatedText = translations[lang][key] || translations['en'][key] || el.textContent;
        // Handle line breaks for ADR title
        if (key === 'adr_title') {
          el.innerHTML = translatedText.replace(/\n/g, '<br>');
        } else {
          el.textContent = translatedText;
        }
      });
      // Team bubble
      document.getElementById('team-bubble-text').textContent = translations[lang]['team_bubble'] || translations['en']['team_bubble'];
      // Footer copyright
      document.getElementById('footer-copyright').textContent = translations[lang]['footer_copyright'] || translations['en']['footer_copyright'];
      // Catalog alphabet
      if (document.getElementById('catalog').style.display !== 'none') setupCatalogAlphabet();
      // If an article modal is open, re-render it with the new language
      try {
        const modalEl = document.getElementById('articleModal');
        if (window.__articleModal__ && modalEl && modalEl.classList.contains('active')) {
          const articleType = window.__articleModal__.currentArticleOpen;
          if (articleType) {
            const data = window.__articleModal__.buildArticleData(articleType);
            window.__articleModal__.renderModal(data);
          }
        }
      } catch (e) { /* no-op */ }
    }
    function showPage(pageId) {
      document.querySelectorAll('.page-section').forEach(sec => sec.classList.add('hidden'));
      if (document.getElementById(pageId)) {
        document.getElementById(pageId).classList.remove('hidden');
      }
      document.querySelectorAll('nav a').forEach(a => a.classList.remove('active'));
      document.querySelectorAll('nav a').forEach(a => {
        if (a.getAttribute('onclick') === `showPage('${pageId}')`) a.classList.add('active');
      });
      if (pageId === 'catalog') setupCatalogAlphabet();
      window.scrollTo({top:0,behavior:'smooth'});
    }
