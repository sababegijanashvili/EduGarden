window.__languageChangeCallbacks = [];
window.registerLanguageChange = function (cb) { window.__languageChangeCallbacks.push(cb); };
let currentLang = 'ge';
window.currentLang = currentLang;

function switchLanguage(lang) {
  currentLang = lang;
  window.currentLang = lang;
  localStorage.setItem('lang', lang);
  // Update document language attribute for accessibility/SEO
  document.documentElement.setAttribute('lang', lang === 'ge' ? 'ka' : 'en');
  document.body.className = lang === 'ge' ? 'georgian' : '';
  document.getElementById('lang-en').classList.toggle('active', lang === 'en');
  document.getElementById('lang-ge').classList.toggle('active', lang === 'ge');
  document.getElementById('lang-en').setAttribute('aria-pressed', lang === 'en');
  document.getElementById('lang-ge').setAttribute('aria-pressed', lang === 'ge');
  // Update all translatable elements
  document.querySelectorAll('[data-translate]').forEach(el => {
    const key = el.getAttribute('data-translate');
    const translatedText = translations[lang][key] || translations['en'][key] || el.textContent;
    // Handle line breaks for ADR title
    if (key === 'adr_title') {
      el.innerHTML = translatedText.replace(/\n/g, '<br>');
    } else {
      el.textContent = translatedText;
    }
  });
  // Team bubble
  const teamBubble = document.getElementById('team-bubble-text');
  if (teamBubble) teamBubble.textContent = translations[lang]['team_bubble'] || translations['en']['team_bubble'];
  // Footer copyright
  const footerCopy = document.getElementById('footer-copyright');
  if (footerCopy) footerCopy.textContent = translations[lang]['footer_copyright'] || translations['en']['footer_copyright'];
  // Trigger any registered language change callbacks
  if (window.__languageChangeCallbacks) {
    window.__languageChangeCallbacks.forEach(cb => {
      try { cb(); } catch (e) { console.error('Language change callback error:', e); }
    });
  }
  if (document.getElementById('catalog').style.display !== 'none') setupCatalogAlphabet();
  // If an article modal is open, re-render it with the new language
  try {
    const modalEl = document.getElementById('articleModal');
    if (window.__articleModal__ && modalEl && modalEl.classList.contains('active')) {
      const articleType = window.__articleModal__.currentArticleOpen;
      if (articleType) {
        const data = window.__articleModal__.buildArticleData(articleType);
        window.__articleModal__.renderModal(data);
      }
    }
  } catch (e) { /* no-op */ }
}

function showPage(pageId) {
  document.querySelectorAll('.page-section').forEach(sec => sec.classList.add('hidden'));
  if (document.getElementById(pageId)) {
    document.getElementById(pageId).classList.remove('hidden');
  }
  document.querySelectorAll('nav a').forEach(a => a.classList.remove('active'));
  document.querySelectorAll('nav a').forEach(a => {
    if (a.getAttribute('onclick') === `showPage('${pageId}')`) a.classList.add('active');
  });
  if (pageId === 'catalog') setupCatalogAlphabet();
  window.scrollTo({ top: 0, behavior: 'smooth' });
}

function loadSiteSettings() {
  if (!window.supabaseClient) {
    setTimeout(loadSiteSettings, 100);
    return;
  }
  window.supabaseClient
    .from('site_settings')
    .select('*')
    .then(({ data, error }) => {
      if (error || !data || data.length === 0) return;
      const settings = data[0];
      
      if (settings.email) {
        const emailEl = document.querySelector('.contact-badge.email .badge-value');
        if (emailEl) emailEl.textContent = settings.email;
      }
      
      if (settings.phone) {
        const phoneEl = document.querySelector('.contact-badge.phone .badge-value');
        if (phoneEl) phoneEl.textContent = settings.phone;
      }
      
      if (settings.address) {
        const addr = settings.address;
        translations.en.mission_location_desc = `Our enterprise is located at ${addr}, next to Gori N8 Public School. The site is convenient for both locals and visitors, and helps promote environmental education in the region.`;
        translations.ge.mission_location_desc = `ჩვენი საწარმო მდებარეობს მისამართზე: ${addr}, გორის N8 საჯარო სკოლის გვერდით. ადგილი მოსახერხებელია როგორც ადგილობრივებისთვის, ასევე სტუმრებისთვის და ხელს უწყობს გარემოსდაცვითი განათლების ხელშეწყობას რეგიონში.`;
      }
      
      if (settings.mission_en) {
        translations.en.mission_details_desc = settings.mission_en;
      }
      
      if (settings.mission_ge) {
        translations.ge.mission_details_desc = settings.mission_ge;
      }
      
      // Re-apply language translations to currently visible settings elements
      const current = window.currentLang || 'ge';
      document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        if (key === 'mission_details_desc' || key === 'mission_location_desc') {
          el.textContent = translations[current][key] || translations['en'][key];
        }
      });
    });
}

// Initialize language on DOMContentLoaded
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('lang') || 'ge';
  switchLanguage(savedLang);
  loadSiteSettings();
});